void main() {
  
  // int, String are the data types
  // age, age1, name, name1 are the variable names
  // 20, 21, Rishin, Ali are the values assigned to the variables

  // Here the data type is strongly typed as 'int'
  int age = 20;
  
  // This will be inferred as int automatically
  var age1 = 21; 

  // Here the data type is strongly typed as 'String'
  String name = 'Rishin';

  // This will be inferred as String automatically
  var name1 = 'Ali';
}